import { CreateSampleCommand } from '../command/createSample.command';
import CreateSampleOptions from 'src/shared/type/sampleOptions.interface';

export class CreateSampleCommandAdapter {
  static toOptions(command: CreateSampleCommand): CreateSampleOptions {
    // command => options 객체 변환
    const data: CreateSampleOptions = {
      pokedex: command.pokedex,
      title: command.title,
      ability: command.ability,
      item: command.item,
      content: command.content,
      tera: command.tera,
      evs: command.evs,
      ivs: command.ivs,
      id: command.id,
      password: command.password,
      party_tag: command.party_tag,
      sample_tag: command.sample_tag,
    };
    return data;
  }
}
